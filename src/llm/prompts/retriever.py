"""
================================================================================
文件角色：Retriever（检索评估器）Prompt 模板
================================================================================
- 被调用者：在 Planner 拆解任务并执行检索后，Retriever Agent 负责评估检索结果
  质量，判断是否需要重试或改写查询。
- 调用者：编排引擎（orchestrator）调用 build_retriever_eval_messages() 构造
  messages，通过 LLMProvider 发送。
- 项目角色：充当检索结果的"质量守门员"——检索结果不够好就触发改写查询重试，
  避免低质量的检索结果让后续 Writer 生成不合格的计划。
================================================================================
"""

# ---------------------------------------------------------------------------
# RETRIEVER_SYSTEM: Retriever Agent 的系统提示词
# ---------------------------------------------------------------------------
# 角色定位：动作检索专家，负责评估检索结果，不负责执行检索本身。
# 输入来源：原始查询词 + 检索系统返回的结果列表。
# 输出用途：产出一个质量评估 JSON，包含评分、缺失项、改写建议。
# 关键字段说明：
#   - quality_score: 0.0~1.0 的浮点数，表示检索结果与用户需求的匹配程度
#   - missing_aspects: 列出现有结果中遗漏的方向或动作类型
#   - rewritten_query: 如果质量不够，提供一个优化后的查询词给检索系统重试；
#                      null 表示无需重试
# 注意：Prompt 要求 LLM 只能评估，不能凭空编造动作，这是安全底线。
# ---------------------------------------------------------------------------
RETRIEVER_SYSTEM = """你是动作检索专家。根据 Planner 的指令，评估检索结果的质量。

判断标准：
- 检索到的动作是否匹配用户需求（目标肌肉、器械、难度）
- 结果是否足够全面（推/拉/腿各方向覆盖）
- 是否有明显的安全隐患（伤病冲突）

输出 JSON：
{
  "quality_score": 0.0-1.0,
  "missing_aspects": ["缺少肩部推举动作"],
  "rewritten_query": "优化后的查询词" | null
}
"""


def build_retriever_eval_messages(original_query: str, results: list) -> list:
    """
    构造发送给 Retriever Agent 的评估消息。

    参数：
        original_query: str  - 原始检索查询词（由 Planner 或 Keyword 匹配生成）
        results: list        - 检索系统返回的结果列表，每项通常包含动作名称、
                               目标肌肉、器械、难度等字段

    返回值：
        list                 - OpenAI 格式的 messages 列表

    核心逻辑：
        将原始查询和检索结果一同喂给 LLM，让它判断"这些结果能不能满足用户需求"。
        如果 quality_score < 阈值，编排引擎会用 rewritten_query 重新检索。
    """
    return [
        {"role": "system", "content": RETRIEVER_SYSTEM},
        {"role": "user", "content": f"查询：{original_query}\n检索结果：{results}"}
    ]
